const macro_t *action_get_macro(keyrecord_t *record, uint8_t id, uint8_t opt) {
	keyevent_t event = record->event;

	switch (id) {
		case 0:
			if (record->event.pressed) {
				return MACRO( D(LSFT), T(A), U(LSFT), END );
			}
			break;
	}
	return MACRO_NONE;
}

